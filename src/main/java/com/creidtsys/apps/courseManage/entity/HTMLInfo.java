package com.creidtsys.apps.courseManage.entity;

public class HTMLInfo {
	private String allCourse;
	private String choosed ;
	private String unChoosed ;
	private String positionName ;
	public String getAllCourse() {
		return allCourse;
	}
	public void setAllCourse(String allCourse) {
		this.allCourse = allCourse;
	}
	public String getChoosed() {
		return choosed;
	}
	public void setChoosed(String choosed) {
		this.choosed = choosed;
	}
	public String getUnChoosed() {
		return unChoosed;
	}
	public void setUnChoosed(String unChoosed) {
		this.unChoosed = unChoosed;
	}
	public String getPositionName() {
		return positionName;
	}
	public void setPositionName(String positionName) {
		this.positionName = positionName;
	}
}	
