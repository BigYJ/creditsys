package com.creidtsys.apps.manage.entity;

public class PaperDetial extends Paper {
	private String detialId ;
	private String detialName ;
	private String detialPaperId ;
	private String detialType ;
	private String detialDesc ;
	private String detialResult ;
	private String detialRate ;
	private String detialDegree ;
	

	public String getDetialName() {
		return detialName;
	}
	public void setDetialName(String detialName) {
		this.detialName = detialName;
	}
	public String getDetialId() {
		return detialId;
	}
	public void setDetialId(String detialId) {
		this.detialId = detialId;
	}
	public String getDetialPaperId() {
		return detialPaperId;
	}
	public void setDetialPaperId(String detialPaperId) {
		this.detialPaperId = detialPaperId;
	}
	public String getDetialType() {
		return detialType;
	}
	public void setDetialType(String detialType) {
		this.detialType = detialType;
	}
	public String getDetialDesc() {
		return detialDesc;
	}
	public void setDetialDesc(String detialDesc) {
		this.detialDesc = detialDesc;
	}

	public String getDetialResult() {
		return detialResult;
	}
	public void setDetialResult(String detialResult) {
		this.detialResult = detialResult;
	}
	public String getDetialRate() {
		return detialRate;
	}
	public void setDetialRate(String detialRate) {
		this.detialRate = detialRate;
	}
	public String getDetialDegree() {
		return detialDegree;
	}
	public void setDetialDegree(String detialDegree) {
		this.detialDegree = detialDegree;
	}
	
}
