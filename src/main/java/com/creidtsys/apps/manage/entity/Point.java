package com.creidtsys.apps.manage.entity;

public class Point {
	private String pointId ;
	private String pointName ;
	private String pointCourseId ;
	private String pointRatio ;
	public String getPointId() {
		return pointId;
	}
	public void setPointId(String pointId) {
		this.pointId = pointId;
	}
	public String getPointName() {
		return pointName;
	}
	public void setPointName(String pointName) {
		this.pointName = pointName;
	}
	public String getPointCourseId() {
		return pointCourseId;
	}
	public void setPointCourseId(String pointCourseId) {
		this.pointCourseId = pointCourseId;
	}
	public String getPointRatio() {
		return pointRatio;
	}
	public void setPointRatio(String pointRatio) {
		this.pointRatio = pointRatio;
	}
	
}
