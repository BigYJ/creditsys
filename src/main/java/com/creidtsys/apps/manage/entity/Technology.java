package com.creidtsys.apps.manage.entity;

public class Technology {
	private String technologyId ;
	private String technologyName ;
	private String technologyInfo ;
	private String technologyRemake ;
	
	public String getTechnologyRemake() {
		return technologyRemake;
	}
	public void setTechnologyRemake(String technologyRemake) {
		this.technologyRemake = technologyRemake;
	}
	public String getTechnologyId() {
		return technologyId;
	}
	public void setTechnologyId(String technologyId) {
		this.technologyId = technologyId;
	}
	public String getTechnologyName() {
		return technologyName;
	}
	public void setTechnologyName(String technologyName) {
		this.technologyName = technologyName;
	}
	public String getTechnologyInfo() {
		return technologyInfo;
	}
	public void setTechnologyInfo(String technologyInfo) {
		this.technologyInfo = technologyInfo;
	}
	
}
